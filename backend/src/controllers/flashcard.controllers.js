import Flashcards from "../models/flashcard.models.js";
import { apierror } from "../utils/apierror.js";
import { apiresponse } from "../utils/apiresponse.js";

//get all flashcard of document
const getflashcards=async(req,res,next)=>{
   try {
     const documentid=req.params.id;
     if(!documentid){
         throw new apierror(400,"document id is not found");
     }
     const flashcards=await Flashcards.find({documentid:documentid,userid:req.user._id})
     .populate('documentid','title filename')
     .sort({createdAt:-1});
     
     res.status(200).json(
         new apiresponse(200,"flashcard fetch sucessfully",{flashcards,count:flashcards.length})
     )
   } catch (error) {
    next(error);
   }

}
